     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function list(Request $request)
    {
        $page = $request->page ? (int)$request->page : 0;
        $limit = $request->limit ? (int)$request->limit : 20;
        $products = Product::where('status', 1)->orderBy('id','desc')->skip($page*$limit )->take($limit)->get();
        
        $data = [];
        $data['count'] = Product::count();
        $data['page'] = $page;
        $data['limit'] = $limit;
        $data['items'] = $products;
        return $this->responseOK($data);
    }


    public function detail($id, Request $request)
    {
        $product = Product::where('id', $id)->orderBy('id','desc')->first();
        return $this->responseOK($product);
    }

